$(document).ready(function() {
    // Create tab for home button
    $('.nav').append(`<li class="active"><a data-toggle='tab' href='#home'>Home <i class="fa fa-home" aria-hidden="true"></i></a></li>`);
    // make navugation tabs work
    $('.nav-tabs a').click(function (e) {
        e.preventDefault()
        $(this).tab('show')
    })
    // create navigation tabs for each sign
    signs.forEach( ({ name, icon }) => {
        $('.nav').append(`<li><a data-toggle='tab' href='#${name}'>${name} ${icon}</a></li>`);
    })
    // create an accordion for each sign
    signs.forEach( ({ name, thumbnail, dates, summary, romance, career, stats, traits }) => {
        $('.tab-content').append(`
            <div id="${name}" class="tab-pane fade">
                <div class="panel-group" id="accordion" role="tablist" aria-multiselectable="true">
                    <div class="panel panel-default">
                        <div class="panel-heading" role="tab" id="heading${name}">
                            <h4 class="panel-title">
                                <a role="button" data-toggle="collapse" data-parent="#accordion" href="#collapse${name}" aria-expanded="true" aria-controls="collapse${name}">
                                    ${name}: <span class="dates">${dates}</span>
                                </a>
                            </h4>
                        </div>
                        <div id="collapse${name}" class="panel-collapse collapse in" role="tabpanel" aria-labelledby="heading${name}">
                            <div class="panel-body">
                                <div class="signHeading">
                                    <div class="headingImage">
                                        <img src="${thumbnail}" alt="${name}" class="summaryImage">
                                    </div>
                                    <div class="headingText">
                                        <h1>${name}</h1>
                                        <h2>${dates}</h2>
                                    </div>
                                </div>
                            </div>
                        </div>
                    </div>
                </div>
            </div>
        `);
    })
})